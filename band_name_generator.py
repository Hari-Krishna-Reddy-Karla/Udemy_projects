"""This project helps to learn about input-function and print-function

Takes the input of city' name and pet's name and outputs the concatenation of those strings.
"""

city = input("Welcome to the Band Name Generator.\n What's the name of the city you grew up in? \n ")
pet = input("What's the name of your pet? \n")

