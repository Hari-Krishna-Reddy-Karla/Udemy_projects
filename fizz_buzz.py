"""This project helps in understanding the usage of elif-statement

Also helps to learn debugging and the sequencing of logic.
"""

for number in range(1, 101):
  if number % 3 == 0 and number % 5 == 0:
    print("FizzBuzz")
  elif number % 3 == 0:
    print("Fizz")
  elif number % 5 == 0:
    print("Buzz")
  else:
    print(number)